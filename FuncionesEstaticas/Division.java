/**
 * funcion que hace la division de numeros
 */

 public class Division {
   /**
    * funcion que hace la division de un numero entero
    * @param x
    * @return x
    */
   public static int division (int x, int y){
      int result = x/y;
      return result;
    }
    /**
     * funcion que hace la division de numeros flotantes
     * @param x
     * @return x
     */
    public static float division (float x, float y){
      float result = x/y;
      return result;
      }
 }
